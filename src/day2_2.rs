enum Command
{
    Forward(u32),
    Down(u32),
    Up(u32),
}

#[derive(Debug)]
struct Position
{
    horizontal: u32,
    depth: u32,
    aim: u32,
}

fn calculate_new_position(pos: &mut Position, comm: &Command)
{
    match comm {
        Command::Forward(val) => {
            pos.horizontal += val;
            pos.depth += pos.aim * val;
        }
        Command::Down(val) => pos.aim += val,
        Command::Up(val) => pos.aim -= val,
    }
}

